#!/bin/bash

# Mood Tracker Standalone Setup Script
echo "Setting up Mood Tracker Standalone Version..."

# Create project directory
mkdir -p mood-tracker-standalone
cd mood-tracker-standalone

# Create project structure
mkdir -p src/{components/{ui},hooks,lib,pages}
mkdir -p public

# Copy package.json for standalone
cat > package.json << 'EOF'
{
